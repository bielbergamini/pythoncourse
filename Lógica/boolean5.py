# Tipo de dado bool (boolean) ao questionar algo em um programa
# Só existem duas respostas possíveis : sim (true) ou não (false).
# Existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual a outro
print(10 == 10) #True
print(10 == 11)#False
print(type(10 == 10))