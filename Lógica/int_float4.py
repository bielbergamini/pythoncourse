# Tipos int e float
# int -> Numero inteiro
# O tipo int representa qualquer numero positivo ou negativo. int sem sinal é considerado positivo
print(11)#int
print(-11)#int
print(0)
# float -> Número com ponto flutuante
# o tipo float representa qualquer número positivo ou negativo com ponto flutuante(número decimal).
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)
# A função type mostra o tipo de dado que o python inferiu ao valor
print(type("Gabriel"))
print(type(1))
print(type(1.1), type(-1.1), type(0.0))